OSErr DrvrCtl(CntrlParamPtr pb, AuxDCEPtr dce) {
    
    OSErr ret;

    GlobalHdl globsHdl = (GlobalHdl)dce->dCtlStorage;
    if (!dce->dCtlStorage || !(*globsHdl)) { ret = controlErr; RETURN_FROM_DRIVER; } // die HARD, should never happen
    GlobalPtr globs = (GlobalPtr)*globsHdl;
    
    switch (pb->csCode) {
        case drvCtl_Format:
        case drvCtl_Verify:         // do nothing; HFS will write the sectors it needs to only rather than zero the disk
            breakReturn(noErr);  
        case drvCtl_infoCC:                
            *((uint32_t*)&pb->csParam) = 0b011000000001; // Unspecified drive type, internal, SCSI, fixed, primary drive. IM 5 p471, PB 150 Devnote
            breakReturn(noErr);  
        case drvCtl_iconCC:
        case drvCtl_iconLogCC:
            *((Ptr*)&(pb->csParam)) = (Ptr)&DiskIcon;
            breakReturn(noErr);  
        case drvCtl_goodbye: // occurs at shutdown and other times the driver MIGHT go away.
            /* make ready for shutdown! flush and disable cache.
            sys 6 does call this after finder appears though... */
            breakReturn(noErr);  
        case drvCtl_Eject: // happens at shutdown (7.5+) or if not bootable. either way drive will go away until an insert event is posted
            breakReturn(noErr);
        default: //  return controlErr for unimplemented codes
            breakReturn(controlErr);
    }

    RETURN_FROM_DRIVER;
}
